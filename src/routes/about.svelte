<h2>A Letter From Devin</h2>

<p>
  My name is Devin Fraze. I run a local Go Club in Columbus Ohio that meets
  three times a week. I founded the Ohio Go Association, a roundtable of
  organizers in my region, to support and learn from others near me. And still
  hungry for more ways to serve, I ran for and was elected to the board of the
  American Go Association.
</p>

<p>
  I'm also passionate about teaching this amazing game. For the past 5 years,
  I've volunteered as the co-Youth Director at the US Go Congress and I teach or
  have taught Weiqi at 5 schools in my city. I also make Baduk content for
  organizers via a blog, youtube channel, and soon a podcast.
</p>

<p>
  As a (near) full-time Baduk organizer, I dream of a world where gathering
  around a goban is the favorite weekly activity of millions. Towards this end,
  I wish to support other organizers in doing whatever is best for their
  communities. Join me as I energize go clubs, produce free baduk media, and map
  the weiqi world!
</p>

<h2>Some words about Derek (from Devin)</h2>

<p>
  Derek doesn't actually play Baduk. I got him to try it once, but it didn't
  stick. So why would I want to work with him? Because, I can't think of a more
  ethical, hard working, and talented person. Derek really likes to help people
  (and I don't just mean close friends). After many conversations with Derek, I
  helped him see a path toward making a truly meaningful difference in the Baduk
  community. And so that's why he's here.
</p>

<p>
  Previously, he worked as an employee of a big tech company but stepped away
  once he'd paid off his debt. He now spends his life empowering others and has
  become hyper concerned with things like personal data privacy and, perhaps
  more importantly, how tech effects our every day life.
</p>

<p>
  To give an example: Not long ago I was overly excited about a feature for
  BadukClub and told him “people will love this so much they'll get addicted!”
  Derek threatened to abandon that feature. To be clear, I don't actually want
  BadukClub to become addicting, I was just a bit too loose with my words. But
  Derek speaks with precision and cares deeply about our users.
</p>

<p>
  So you can rest easy my dear Baduk friends. I'm going to work tirelessly with
  you to build a better Go world. And Derek is going to be here with us, day
  after day, doing the hard things necessary to make the dream come true and to
  keep us safe while we venture into a brighter future.
</p>

